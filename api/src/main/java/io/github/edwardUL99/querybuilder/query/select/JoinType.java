package io.github.edwardUL99.querybuilder.query.select;

/**
 * Represents the possible join types
 */
public enum JoinType {
    /**
     * Inner join
     */
    INNER,
    /**
     * Left outer join
     */
    LEFT,
    /**
     * Right outer join
     */
    RIGHT,
    /**
     * Full outer join
     */
    FULL
}
